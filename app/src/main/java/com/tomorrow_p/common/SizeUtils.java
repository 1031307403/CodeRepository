    /**
     * @return 是否是浮点数
     */
    public static boolean isFloat(String string) {
        String regex = "^[-+]??(\\d++[.]\\d*?|[.]\\d+?)([eE][-+]??\\d++)?$";
        Pattern pattern = Pattern.compile(regex);
        return pattern.matcher(string).matches();
    }

